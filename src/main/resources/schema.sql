create table country (
id int GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) primary key,
name varchar(50));

create table factory (
id int GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) primary key,
name varchar(50),
country_id varchar(50));

create table line (
	id int GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) primary key,
	name varchar(20) ,
	product_id int,
	speed int,
	unit varchar(20),
	capacity int,
	build_year int ,
	factory_id int );
	
	CREATE TABLE report (
	id int GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) primary key,
	date DATE ,
	shift int ,
	register int  ,
	input int ,
	output int ,
	line_id int );
	
	CREATE TABLE consumption (
	id int GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) primary key,
	report_id int ,
	material_id int ,
	quantity FLOAT );
	
	CREATE TABLE material (
	id int GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) primary key,
	name varchar(20) ,
	unit varchar(20) ,
	deleted boolean );
	
	CREATE TABLE product (
	id int GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) primary key,
	name varchar(20) ,
	groupe int );
	
	